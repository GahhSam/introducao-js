let num = prompt("Digite um número:");

if (num > 0 && num>=100) {
    console.log("O número é positivo e maior que 100.");
} 
else if (num < 0) {
    console.log("O número é negativo.");
}
 else if (num >100) {
    console.log("O número é negativo.");
}
else {
    console.log("O número é zero.");
}
